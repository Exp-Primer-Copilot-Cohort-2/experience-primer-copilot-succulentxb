function skillsMember() {
    var member = document.getElementById("member");
    var memberSkills = document.getElementById("memberSkills");
    var memberSkillsContent = document.getElementById("memberSkillsContent");
    var memberSkillsContentClose = document.getElementById("memberSkillsContentClose");
    memberSkills.style.display = "block";
    memberSkillsContent.style.display = "block";
    memberSkillsContentClose.style.display = "block";
    member.style.display = "none";
}